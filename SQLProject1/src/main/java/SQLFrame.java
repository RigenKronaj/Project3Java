    private void DisplayTable()
    {
        try{
            st = conn.createStatement();
            String sql = "select * from libri";
            rs = st.executeQuery(sql);
            
            while(rs.next()){
                String ID = String.valueOf(rs.getInt("ID"));
                String Titull = rs.getString("Titull");
                String Autori = rs.getString("Autori");
                String Viti_i_botimit = String.valueOf(rs.getInt("Viti_i_botimit"));
                String Shtepia_botuese = rs.getString("Shtepia_botuese");
                String Cmimi = String.valueOf(rs.getInt("Cmimi"));
                String Numri_i_kopjeve = String.valueOf(rs.getInt("Numri_i_kopjeve"));
                
                String tbData[] = {ID, Titull, Autori, Viti_i_botimit, Shtepia_botuese, Cmimi, Numri_i_kopjeve};
                DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
                
                tblModel.addRow(tbData);
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
    
